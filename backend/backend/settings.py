# Determine log levels based on environment
# For staging/production: Use WARNING to reduce log volume
# For development: Use DEBUG/INFO for detailed logging
def get_log_level(default_level="INFO"):
    """
    Get log level from environment variable.

    Usage in .env files:
    - Development: LOG_LEVEL=DEBUG
    - Staging: LOG_LEVEL=WARNING  (recommended to reduce noise)
    - Production: LOG_LEVEL=WARNING

    Valid levels: DEBUG, INFO, WARNING, ERROR, CRITICAL
    """
    level = os.environ.get("LOG_LEVEL", default_level).upper()
    valid_levels = ["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL"]
    return level if level in valid_levels else default_level


